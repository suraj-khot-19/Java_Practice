package CodeWithSuraj;

public class CWS_16_EscapeSequences {
    public static void main(String[] args) {
        System.out.println("hello my name is : \n Suraj");  //new line
        System.out.println("hello my name is \t Suraj");    //tab
        System.out.println("hello my name is \"Suraj\"");   //double qu
        System.out.println("hello my name is \'Suraj\'");   //single qu
        System.out.println("hello 2 \\ me");               //backslash
        System.out.println("hello my name is : \b Suraj");  //insert backspace
        System.out.println("hello my name is : \r Suraj");  //carriage return
        System.out.println("hello my name is : \f Suraj");  //insert form feed

    }
}
